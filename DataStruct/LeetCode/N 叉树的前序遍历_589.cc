class Node {
   public:
    int val;
    vector<Node*> children;

    Node() {}

    Node(int _val) { val = _val; }

    Node(int _val, vector<Node*> _children) {
        val = _val;
        children = _children;
    }
};

class Solution {
   public:
    void helper(const Node* root, vector<int>& res) {
        if (!root) {
            return;
        }
        res.emplace_back(root->val);
        for (auto& ch : root->children) {
            helper(ch, res);
        }
    }
    vector<int> preorder(Node* root) {
        vector<int> res;
        helper(root, res);
        return res;
    }
};

